  // Áï∂‰∏ãÂãïÊ©ü
  const motivationMatch = body.match(/\*\*üéØ ÁÇ∫‰ªÄÈ∫ºÁèæÂú®ÁâπÂà•ÊÉ≥Â≠∏[^)]*\)\*\*\s*\n\s*(.+?)(?=\n\n|- \*\*)/);
